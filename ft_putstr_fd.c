#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		write (fd, &s[i++], 1);
	}
}
