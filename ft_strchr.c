#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	t;

	t = c;
	i = 0;
	while (s[i] != '\0' || s[i] == t)
	{
		if (s[i] == t)
		{
			return (((char *)s) + i);
		}
		i++;
	}
	return (NULL);
}
